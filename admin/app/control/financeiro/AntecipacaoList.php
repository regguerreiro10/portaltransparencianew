    /**
     * Register the filter in the session
     */
    public function onSearch($param = null)
    {
        $data = $this->form->getData();
        $filters = [];

        TSession::setValue(__CLASS__.'_filter_data', NULL);
        TSession::setValue(__CLASS__.'_filters', NULL);

        if (isset($data->id) AND ( (is_scalar($data->id) AND $data->id !== '') OR (is_array($data->id) AND (!empty($data->id)) )) )
        {

            $filters[] = new TFilter('id', '=', $data->id);// create the filter 
        }

        if (isset($data->intervalo_data_antecipacaof) AND ( (is_scalar($data->intervalo_data_antecipacaof) AND $data->intervalo_data_antecipacaof !== '') OR (is_array($data->intervalo_data_antecipacaof) AND (!empty($data->intervalo_data_antecipacaof)) )) )
        {

            $filters[] = new TFilter('data_antecipacao', '<=', $data->intervalo_data_antecipacaof);// create the filter 
        }

        if (isset($data->intervalo_data_antecipacaoi) AND ( (is_scalar($data->intervalo_data_antecipacaoi) AND $data->intervalo_data_antecipacaoi !== '') OR (is_array($data->intervalo_data_antecipacaoi) AND (!empty($data->intervalo_data_antecipacaoi)) )) )
        {

            $filters[] = new TFilter('data_antecipacao', '>=', $data->intervalo_data_antecipacaoi);// create the filter 
        }

        if (isset($data->forma_pagamento_id) AND ( (is_scalar($data->forma_pagamento_id) AND $data->forma_pagamento_id !== '') OR (is_array($data->forma_pagamento_id) AND (!empty($data->forma_pagamento_id)) )) )
        {

            $filters[] = new TFilter('forma_pagamento_id', '=', $data->forma_pagamento_id);// create the filter 
        }

        if (isset($data->obs) AND ( (is_scalar($data->obs) AND $data->obs !== '') OR (is_array($data->obs) AND (!empty($data->obs)) )) )
        {

            $filters[] = new TFilter('obs', 'like', "%{$data->obs}%");// create the filter 
        }

        // fill the form with data again
        $this->form->setData($data);

        // keep the search data in the session
        TSession::setValue(__CLASS__.'_filter_data', $data);
        TSession::setValue(__CLASS__.'_filters', $filters);

        $this->onReload(['offset' => 0, 'first_page' => 1]);
    }

    /**
     * Load the datagrid with data
     */
    public function onReload($param = NULL)
    {
        try
        {
            // open a transaction with database 'minierp'
            TTransaction::open(self::$database);

            // creates a repository for Antecipacao
            $repository = new TRepository(self::$activeRecord);

            $criteria = clone $this->filter_criteria;

            if (empty($param['order']))
            {
                $param['order'] = 'id';    
            }

            if (empty($param['direction']))
            {
                $param['direction'] = 'desc';
            }

            $criteria->setProperties($param); // order, offset
            $criteria->setProperty('limit', $this->limit);

            if($filters = TSession::getValue(__CLASS__.'_filters'))
            {
                foreach ($filters as $filter) 
                {
                    $criteria->add($filter);       
                }
            }

            $cont = 1;

            // load the objects according to criteria
            $objects = $repository->load($criteria, FALSE);

            $this->datagrid->clear();
            if ($objects)
            {
                // iterate the collection of active records
                foreach ($objects as $object)
                {

                    $row = $this->datagrid->addItem($object);
                    $row->id = "row_{$object->id}";

                    $row = new TTableRow;

                    $div = new TElement('div');
                    $div->id = "container_antecipacao_itens_{$object->id}";
                    /*
                    $container = new BPageContainer();
                    $container->setAction(new TAction(['ContaSimpleList','onReload'],['antecipacao_id'=> $object->id]));
                    $container->setId($div->id);

                    $div->add($container);*/
                    $cell = $row->addCell($div);
                    $cell->colspan = $this->datagrid->getTotalColumns();
                    $cell->style='padding:10px;';

                    $this->datagrid->insert($cont+1,$row);

                    $cont+=3;

                }
            }

            // reset the criteria for record count
            $criteria->resetProperties();
            $count= $repository->count($criteria);

            $this->pageNavigation->setCount($count); // count of records
            $this->pageNavigation->setProperties($param); // order, page
            $this->pageNavigation->setLimit($this->limit); // limit

            // close the transaction
            TTransaction::close();
            $this->loaded = true;

            return $objects;
        }
        catch (Exception $e) // in case of exception
        {
            // shows the exception error message
            new TMessage('error', $e->getMessage());
            // undo all pending operations
            TTransaction::rollback();
        }
    }

